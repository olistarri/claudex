import type { Chat } from './chat.types';
import type { ToolAggregate } from './tools.types';

export type ToolComponent = React.FC<{ tool: ToolAggregate; chatId?: string }>;

export type Theme = 'light' | 'dark';

type MentionType = 'file' | 'agent' | 'prompt';

export interface MentionItem {
  type: MentionType;
  name: string;
  path: string;
  description?: string;
}

export interface ThemeState {
  theme: Theme;
  toggleTheme: () => void;
}

export interface ModelSelectionState {
  selectedModelId: string;
  selectModel: (modelId: string) => void;
}

export interface PermissionModeState {
  permissionMode: 'plan' | 'ask' | 'auto';
  setPermissionMode: (mode: 'plan' | 'ask' | 'auto') => void;
}

export interface ThinkingModeState {
  thinkingMode: string | null;
  setThinkingMode: (mode: string | null) => void;
}

export type ViewType =
  | 'agent'
  | 'browser'
  | 'editor'
  | 'ide'
  | 'terminal'
  | 'secrets'
  | 'webPreview'
  | 'mobilePreview';

export interface SplitViewState {
  isSplitMode: boolean;
  currentView: ViewType;
  secondaryView: ViewType | null;
}

export interface SplitViewActions {
  setCurrentView: (view: ViewType) => void;
  setSecondaryView: (view: ViewType | null) => void;
  exitSplitMode: () => void;
  handleViewClick: (view: ViewType, isShiftClick: boolean) => void;
}

export interface UIState {
  currentChat: Chat | null;
  attachedFiles: File[];
  sidebarOpen: boolean;
}

export interface UIActions {
  setAttachedFiles: (files: File[]) => void;
  setCurrentChat: (chat: Chat | null) => void;
  setSidebarOpen: (isOpen: boolean) => void;
}

export interface SlashCommand {
  value: string;
  label: string;
  description?: string;
}
